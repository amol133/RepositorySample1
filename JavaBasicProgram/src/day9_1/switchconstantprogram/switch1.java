package day9_1.switchconstantprogram;

public class switch1 {

	public static void main(String[] args) {
	  
		int value=11;
		
		switch (value) {
		case 3:
		      System.out.println("hello here is the case 3 executed");

		      break;
		  
		case 4:
		      System.out.println("hello here is the case 4 executed");
          
		      break;
		  
		case 7:
		      System.out.println("hello here is the case 7 executed");
              break;
        
		case 10:
		      System.out.println("hello here is the case 10 executed");
              break;
		  
	    default:
		      System.out.println("your case not match");
              break;
		  
		  
		}

	}

}
