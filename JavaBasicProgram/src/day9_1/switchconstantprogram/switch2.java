package day9_1.switchconstantprogram;

public class switch2 {

	public static void main(String[] args) {
		  int month=3;
		  
		  switch(month) {
		  case 1:
		      System.out.println("hello here is the month 1 january");
              break;
		  case 3:
		      System.out.println("hello here is the month 3 march");
              break;
		  case 5:
		      System.out.println("hello here is the month 5 may");
              break;
		  case 7:
		      System.out.println("hello here is the month 7 july");
              break;
		  default:
		      System.out.println("hello no cases matches");
              break;
		  
		  
		  
		  
		  }

	}

}
