public class Forloops2 {

	public static void main(String[] args) {
	
		for (char c='a'; c<='z'; c++) {
			System.out.println("c="+c);

		}
		System.out.println("*************printing in capital A TO Z*********");

		for (char c1='Z'; c1 >='A'; c1--) {// reverse and sequential both run
			System.out.println("c1 ="+ c1);

		}
		
	for(int i=10;i>=0;i--) {
		System.out.println(i);
	}
		
		
		
		for (char c='a';c<='z';c++) {
			System.out.println(c);
		}
		for (int i=10;i>=0;i--) {
			System.out.println(i);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
