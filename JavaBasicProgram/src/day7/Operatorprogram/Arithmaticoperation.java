package day7.Operatorprogram;

public class Arithmaticoperation {

	public static void main(String[] args) {
	// variable declare and initialization
		
		int num1=55,num2=10,res;
		// addition operation
		res=num1+num2;
		System.out.println("addition of the number ="+ res);
		
		// subtraction
		res=num1-num2;
		System.out.println("subtraction of both =" + res);
		
		// multiplication
		res=num1*num2;
		System.out.println("multiplication of both = " + res);
		
		// division 
		res=num1/num2;
		System.out.println("division of both number="+res);
		
		// mode or modulus
		res=num1%num2;
		System.out.println("modulus of the both number ="+ res);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
